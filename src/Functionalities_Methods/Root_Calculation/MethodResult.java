
package Functionalities_Methods.Root_Calculation;


public class MethodResult {
    
    
  
    double Xvalue;
    double Em;

    public MethodResult(Double Xvalue, Double Em) {

        this.Xvalue = Xvalue;
        this.Em = Em;
    }

    public double getXvalue() {
        return Xvalue;
    }

    public double getEm() {
        return Em;
    }
}
