    private void jSpinnerGradoStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSpinnerGradoStateChanged

        DefaultTableModel modelo = (DefaultTableModel) TablaValores.getModel();
        DefaultTableModel modeloSolt = (DefaultTableModel) Soluciones.getModel();
        DefaultTableModel modelAproX = (DefaultTableModel) Aproximaciones.getModel();
        int ecuaciones = modelo.getColumnCount();
        int total = (int) jSpinnerGrado.getValue();

        if(ecuaciones < total){
            for(int i = ecuaciones ; i < total ; i++){
                modelo.addColumn("X"+i);
                modelo.addRow(new Vector());
                modeloSolt.addRow(new Vector());
                String temp [] = {"0"}; 
                modelAproX.addRow(temp);
            }
        }
        else if(ecuaciones > total){
            modelo.setColumnCount(total);
            modelo.setRowCount(total);
            modeloSolt.setRowCount(total);
            modelAproX.setRowCount(total);
            
        }

        this.CentrarTabla(TablaValores);
        this.CentrarTabla(Soluciones);
        this.CentrarTabla(Aproximaciones);
    }//GEN-LAST:event_jSpinnerGradoStateChanged

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        int size = (int) jSpinnerGrado.getModel().getValue();
        
        double value_x[][] = new double[size][size];
        double value_y[] = new double[size];
        double inicial[] = new double[size];
        
        for(int i = 0 ; i < size ; i ++){
            for(int j = 0 ; j < size ; j++){
                value_x[j][i] =  Double.valueOf((String)TablaValores.getValueAt(j, i));
            }
            value_y[i] =  Double.valueOf((String)Soluciones.getValueAt(i, 0));
            inicial[i] = Double.valueOf((String)Aproximaciones.getValueAt(i, 0));
        }
        
        
        if(jSidel.isSelected()){
        Gauss_Seidel temp = new Gauss_Seidel();
        temp.GaussSeidel(value_x, value_y, Double.valueOf(Tolerancia.getText()),inicial);
        Pizarra.setText(temp.getsteps(formato));
        }
        else{
            Jacobi temp = new Jacobi();
            temp.jacobi(value_x, value_y, Double.valueOf(Tolerancia.getText()), inicial);
            Pizarra.setText(temp.getsteps(formato));
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jSpinnerDecimalesStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSpinnerDecimalesStateChanged

        
        int value = (int) jSpinnerDecimales.getValue();
        if (value > decimales.length()) {
            decimales = "#.";
            for (int i = 0; i < value ; i++) {
                decimales = decimales + "#";
            }
        } else if (value < decimales.length()) {

            decimales = "#.";
            for (int i = 0; i < value ; i++) {
                decimales = decimales + "#";
            }
        }
        this.formato = new DecimalFormat(decimales);
    }//GEN-LAST:event_jSpinnerDecimalesStateChanged


